function add(a: number, b: number): number {
  return a + b;
}

function substract(a: number, b: number): number {
  return a - b;
}

function multiply(a: number, b: number): number {
  return a * b;
}

function divide(a: number, b: number): number {
  return a / b;
}

export { add, substract, multiply, divide };
